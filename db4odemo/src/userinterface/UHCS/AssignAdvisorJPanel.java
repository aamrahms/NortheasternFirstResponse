    public void populateTable() {
        DefaultTableModel md=(DefaultTableModel)tblAdvisorList.getModel();
        md.setRowCount(0);
        Object row[]= new Object[3];
        
        //taking network, enterprise level doctor list
            ArrayList<Advisor> advisorList  = system.getAdvisorDirectory().getAdvisorList();
        for (Advisor advisor : advisorList) {
            row[0] = advisor; //username gets printed
            row[1] = advisor.getEmployee().getName();
            row[2] = advisor.getEmployee().isIsFree();  //true false
                             
            md.addRow(row);
        }
    }

