    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
        int selectedRow = tblAdvisorList.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select an advisor from the table to assign","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            Advisor advisor  = (Advisor)tblAdvisorList.getValueAt(selectedRow, 0);
            boolean isFree = (boolean) tblAdvisorList.getValueAt(selectedRow, 2);
            
            if(!isFree) {
                JOptionPane.showMessageDialog(null,"Sorry selected advisor is not free, select another advisor who is free","Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
            //Set status of complaint
            complaintWorkRequest.setStatus("UHCS Advisor Assigned and Doctor Completed");
            
            //set AdvisorName on complaint
            //complaintWorkRequest.setAdvisor(advisor.getEmployee().getUsername());
            
            //assign it in enterprise level doctor object
            advisor.setComplaint(complaintWorkRequest);
            //advisor.getCompletedComplaintsList().add(complaintWorkRequest);
            //make doctor busy
            advisor.getEmployee().setIsFree(false);
            
            //assign it in userAccount level of doctor (to do on signup)
            UserAccount advisorUserAccount = system.getUserAccountDirectory().fetchUaWithUserName(advisor.getEmployee().getUsername());
            advisorUserAccount.getWorkQueue().getComplaintList().add(complaintWorkRequest);
            //make ua busy
            advisorUserAccount.getEmployee().setIsFree(false);
            
            JOptionPane.showMessageDialog(null,"Complaint with id: "+ complaintWorkRequest.getComplaintID() +"is assigned to Advisor "+advisor.getEmployee().getName());
                    
            // clear table
            DefaultTableModel model = (DefaultTableModel) tblAdvisorList.getModel();
            model.setRowCount(0);
        
            //back page
            userProcessContainer.remove(this);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.previous(userProcessContainer);

        }

    }//GEN-LAST:event_btnAssignActionPerformed

